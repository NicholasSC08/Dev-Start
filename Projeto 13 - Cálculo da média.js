function obterSomaDeTodasAsNotas(notas) {
let soma = 0
notas.forEach(function(nota){
  soma = soma + nota
})
return soma
}

function obterMediaDeTodasAsNotas(notas) {
 let soma = 0
 let totalNotas = 0
 notas.forEach(function(nota){
   soma = soma += nota
   totalNotas++
 })
 return soma / totalNotas
}
