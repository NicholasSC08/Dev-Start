function obterIniciais(nomes) {
 nomes = nomes.map(function(string){
   string = string[0]
   return string
 })
 return nomes
}

console.log(obterIniciais(["Alice", "Bruno", "Samuel", "Augusto"])) // ["A", "B", "S", "A"]
console.log(obterIniciais(["samara", "alexandre", "carla"])) // ["s", "a", "c"]
